

class PrintStatic:

    @staticmethod
    def printar(text):
        """
        Print the given text to the console.
        """
        print(text)

    @staticmethod
    def print_espaco():
        """
        Print an empty line.
        """
        print("\n")

    @staticmethod
    def print_asteristico():
        """
        Print a line of asterisks.
        """
        print("*" * 7)
